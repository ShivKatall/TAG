#import "UIColor+TAGColors.h"

@implementation UIColor (TAGColors)

+(UIColor *)slateGrayColor
{
    return [UIColor colorWithRed:0.255 green:0.251 blue:0.259 alpha:1];
}

+(UIColor *)linkColor
{
    return [UIColor colorWithRed:0.416 green:0.804 blue:0.910 alpha:1];
}

@end
